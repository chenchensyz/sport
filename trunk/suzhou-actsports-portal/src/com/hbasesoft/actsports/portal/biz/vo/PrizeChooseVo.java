package com.hbasesoft.actsports.portal.biz.vo;

public class PrizeChooseVo {

	private String chooseId;
	
	private String title;
	
	private String prizeType;
	
	private String url;

	private String state;
	
	private String entryprizeName;
	
	public String getChooseId() {
		return chooseId;
	}

	public void setChooseId(String chooseId) {
		this.chooseId = chooseId;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPrizeType() {
		return prizeType;
	}

	public void setPrizeType(String prizeType) {
		this.prizeType = prizeType;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getEntryprizeName() {
		return entryprizeName;
	}

	public void setEntryprizeName(String entryprizeName) {
		this.entryprizeName = entryprizeName;
	}
	
	
}
